export function assignUniqueCoversForList(resources) {
    const used = new Set();
    return resources.map((resource) => {
        const coverImage = pickResourceCoverFromPool(resource, used);
        if (coverImage?.src) {
            used.add(coverImage.src);
        }
        return { ...resource, coverImage };
    });
}

